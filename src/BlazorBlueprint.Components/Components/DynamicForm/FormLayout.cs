namespace BlazorBlueprint.Components;

/// <summary>
/// Defines the layout mode for a dynamic form.
/// </summary>
public enum FormLayout
{
    /// <summary>Fields stacked vertically with labels above (default).</summary>
    Vertical,

    /// <summary>Labels placed to the left of fields.</summary>
    Horizontal,

    /// <summary>Fields arranged inline in a single row.</summary>
    Inline
}
