namespace BlazorBlueprint.Components;

/// <summary>
/// Specifies the horizontal alignment of funnel chart segments.
/// </summary>
public enum FunnelAlign
{
    /// <summary>
    /// Segments are centered horizontally.
    /// </summary>
    Center,

    /// <summary>
    /// Segments are aligned to the left edge.
    /// </summary>
    Left,

    /// <summary>
    /// Segments are aligned to the right edge.
    /// </summary>
    Right
}
